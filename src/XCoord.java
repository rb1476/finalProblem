
public class XCoord {
	private Bar bar;
	private int value;	
	
	public XCoord(Bar bar, int value) {
		this.bar = bar;
		this.value = value;
	}

	public Bar getBar() {
		return bar;
	}
	
	public void setBar(Bar bar) {
		this.bar = bar;
	}
	
	public int getValue() {
		return value;
	}
	
	public void setValue(int value) {
		this.value = value;
	}
	
}
